import numpy as np
"""a function def np_matmul(mat1, mat2): that performs matrix multiplication"""


def np_matmul(mat1, mat2):
    """a function np_matmul(mat1, mat2): that performs matrix multiplication"""
    mat = np.matmul(mat1, mat2)
    return mat
